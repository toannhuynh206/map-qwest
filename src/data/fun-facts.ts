/**
 * Geography fun facts, categorised by game region/mode.
 * Call getRandomFunFact(category) to get a fact relevant to the current game.
 */

import type { QuizRegion } from '@/types/quiz-config';

export type FactCategory =
  | 'us_states'
  | 'world'
  | 'africa'
  | 'europe'
  | 'asia'
  | 'north_america'
  | 'south_america'
  | 'oceania';

export function regionToFactCategory(
  mode: 'countries' | 'states',
  region: QuizRegion,
): FactCategory {
  if (mode === 'states') return 'us_states';
  if (region === 'pin_mini' || region === 'world') return 'world';
  return region as FactCategory;
}

export function getRandomFunFact(category: FactCategory = 'world'): string {
  const pool = FACTS[category];
  return pool[Math.floor(Math.random() * pool.length)];
}

